import java.util.Date
import java.util.Locale
import com.gravity.billeasy.R

@Composable
fun DatePicker(modifier: Modifier = Modifier, lable: String) {
    var selectedDate = remember { mutableStateOf<Long?>(null) }
    var showModal = remember { mutableStateOf(false) }

    OutlinedTextField(
        value = if(selectedDate.value != null) convertMillisToDate(selectedDate.value!!) else "",
        onValueChange = { },
        label = { Text(lable) },
        placeholder = { Text("MM/DD/YYYY") },
        trailingIcon = {
            Icon(painter = painterResource(R.drawable.calendar), contentDescription = "Select date")
        },
        modifier = modifier
            .fillMaxWidth()
            .pointerInput(selectedDate) {
                awaitEachGesture {
                    // Modifier.clickable doesn't work for text fields, so we use Modifier.pointerInput
                    // in the Initial pass to observe events before the text field consumes them
                    // in the Main pass.
                    awaitFirstDown(pass = PointerEventPass.Initial)
                    val upEvent = waitForUpOrCancellation(pass = PointerEventPass.Initial)
                    if (upEvent != null) {
                        showModal.value = true
                    }
                }
            }
    )

    if (showModal.value) {
        DatePickerModal(
            onDateSelected = { selectedDate.value = it },
            onDismiss = { showModal.value = false }
        )
    }
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun DatePickerModal(
    onDateSelected: (Long?) -> Unit,
    onDismiss: () -> Unit
) {
    val datePickerState = rememberDatePickerState()

    DatePickerDialog(
        onDismissRequest = onDismiss,
        confirmButton = {
            TextButton(onClick = {
                onDateSelected(datePickerState.selectedDateMillis)
                onDismiss()
            }) {
                Text("OK")
            }
        },
        dismissButton = {
            TextButton(onClick = onDismiss) {
                Text("Cancel")
            }
        }
    ) {
        DatePicker(state = datePickerState)
    }
}

fun convertMillisToDate(millis: Long): String {
    val formatter = SimpleDateFormat("MM/dd/yyyy", Locale.getDefault())
    return formatter.format(Date(millis))
}